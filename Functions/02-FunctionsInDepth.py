# Scope - Local and Global Scope

# Global Scope
a = 10
b = 12

def add():
    # Local Scope
    # a = 10
    # b = 12
    print(a+b)

def sub():
    # a = 10
    # b = 12
    print(a-b)

def div():
    # a = 10
    # b = 12
    print(a/b)

def mul():
    # a = 10
    # b = 12
    print(a*b)

add()
sub()
div()
mul()
