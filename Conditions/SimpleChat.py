while True:
    userMessage = input("Enter your message : ")

    if userMessage == "Hi" or userMessage == 'hi':
        print("CPU : Hi")

    elif userMessage == "Hey" or userMessage == 'hey':
        print("CPU : Hey")

    elif userMessage == "Good Morning" or userMessage == "good morning":
        print("CPU : Good Morning")

    elif userMessage == "Bye" or userMessage == "bye":
        print("CPU : Bye, Have a nice day...")

    else:
        print("I don't understand")
